<h2>Independent Power Producer</h2>

<div id="project-customer">Servicios Electricos Tarija (SETAR)</div>
<div id="project-location">Yacuiba, Tarija, Bolivia</div>

<ul>
  <li>
    Electrical Studies to obtain Operation Permit to be an IPP (Independent Power Producer) interconnecting to the  Bolivian National Grid. The studies included:
    <ul>
      <li>Short Circuit Studies.</li>
      <li>Power Flows</li>
      <li>Sizing capacities for existing and future expansions of the utility.</li>
      <li>Voltage drop studies for existing Medium Voltage feeders.</li>
    </ul>
  </li>
</ul>