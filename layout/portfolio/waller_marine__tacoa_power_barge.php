<h2>Tacoa Power Barge</h2>

<div id="project-customer">Waller Marine</div>
<div id="project-location">Tacoa, Venezuela</div>

<ul>
  <li>Electrical Completion for two power barges for equipped with one 7FA 200 MW gas turbine from GE. 400 MW total for Tacoa Project, Venezuela.</li>
  <li>Commissioning and start up for both power   barges including: generators, 18 kV generator circuit breaker, 18 kV Iso Phase, 4160 V switchgear, auxiliary diesel genset, AC/Dc auxiliary services, etc.</li>
  <li>Supervision of commissioning for the 230 kV High Voltage Equipment such as: power transformer, Circuit breaker,   air disconnect switches, current transformers, capacitive voltage transformers, protection panels, etc.</li>
  <li>Startup of the entire installation and interconnection to Corpoelec grid.</li>
</ul>

