<h2>Santa Isabel Wind Farm</h2>

<div id="project-customer">Wanzek Construction</div>
<div id="project-location">Santa Isabel, Puerto Rico</div>

<ul>
  <li>Commissioning and Start Up for a 138 kV substation, Breaker and a Half Bus bar configuration as part for the installation of the 100 MW Santa Isabel Wind Farm in Puerto Rico.</li>
  <li>Testing of the complete Utility Substation interconnecting PREPA’s (Puerto Rico Electrical Power Authority) National Grid.</li>
  <li>
    <ul>
      Testing was performed for :
      <li>All High Voltage Equipment, such as: Circuit Breakers, Air Disconnect Switches, current transformers, voltage transformers, lightning arresters, post insulators, etc, performing Power Factor, Primary injection, insulation, functionality, etc.</li>
      <li>All protection panels and protection relays consisting of more than 30 protection relays, among them: SEL311L, SEL 351, SEL587Z, etc.</li>
      <li>All commissioning and Start Up was performed in coordination with PREPA’s engineers (Puerto Rico Electrical Power Authority).</li>
    </ul>
  </li>
  <li>Installation successfully put in service in record time due customer’s requirements.</li>
</ul>