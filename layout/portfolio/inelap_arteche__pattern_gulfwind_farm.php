<h2>Pattern Gulfwind Farm</h2>

<div id="project-customer">Inelap/Arteche</div>
<div id="project-location">Armstrong, Texas</div>

<ul>
  <li>
    Commissioning of the following high voltage apparatus for the expansion of two new capacitor banks:
    <ul>
      <li>2 x 34.6 kV SF6 Circuit Breakers, each one  with 9 bushing type current transformers.</li>
      <li>8 x 34.5 kV Air disconnect switches.</li>
      <li>12 x 34.5 kV earthing disconnect switches, regarding each step for the capacitor bank.</li>
      <li>4 x Capacitor bank 16.5 MVAR (each) including 34.5 kV grounding switch, limiting reactor (air) and CT to detect unbalance for neutral.</li>
      <li>6 x Capacitor bank 8.25 MVAR (each) including 34.5 kV Circuit Switcher, 34.5 kV grounding switch, limiting reactor (air) and CT to detect unbalance current flowing to the  neutral.</li>
      <li>2 x Power Reactors (air) and 34.5 kV disconnect switch.</li>
      <li>10 x34.5 kV Circuit Switchers for each capacitor bank.</li>
      <li>12 x Surge arrester for each 34.5 kV Bus Bar and power reactors.</li>
      <li>18 x 35 kV power cables, including stress cones.</li>
    </ul>
  </li>
  <li>
    Control and Protection Panel consisting of:
    <ul>
      <li>1 x SEL- 451 overcurrent protection relay</li>
      <li>2 x SEL-351, feeder protection relays</li>
      <li>2 x SEL-551, protection relays</li>
      <li>2 x SEL-2505, I/O modules to be integrated with the existing control system.</li>
    </ul>
  </li>
</ul>

