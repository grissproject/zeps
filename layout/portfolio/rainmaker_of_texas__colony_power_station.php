<h2>Colony Power Station</h2>

<div id="project-customer">Rainmaker of Texas</div>
<div id="project-location">Texas, United States</div>

<ul>
  <li>Complete engineering package for Colony Power Station equipped with one 7FA turbine from GE, including entire power block, 18 kV, 4160 V and 480 V auxiliary services, 18 kV ISO phase, and 138 kV substation to interconnect with Centerpoint.</li>
  <li>
    Scope of work:
    <ul>
      <li>Single line diagram, AC and DC Schematics and wiring diagrams for fabrication of protection systems, Automation System for Remote Operation and Data Acquisition, control and protection systems, etc.</li>
      <li>General Development Plan, Plot and Elevation plan, Grading plan (Site Plan, fence layout and details), Foundation plan and details, Grounding plan and installation details, Substation trench, cable trays, conduits plan and details.</li>
      <li>Control house plan, elevations and details, Sizing of station service diagrams for AC and DC systems.</li>
      <li>Bill of Material List.</li>
      <li>Cable schedules and termination list.</li>
      <li>Substation steel erection drawings.</li>
      <li>Assembly of 138 kV substation up to the A frame to interconnect with Centerpoint.</li>
      <li>Electrical Commissioning for entire power plant including, generator, 18 kV system, ISO phase, Generator Breaker, 138 kV substation and functional.</li>
      <li>Short Circuit Studies, Protection Coordination Study and Relay settings.</li>
      <li>Grounding calculations per IEEE 80.</li>
      <li>Lightning protection shield as per of "Rolling Sphere Methods".</li>
    </ul>
  </li>
</ul>