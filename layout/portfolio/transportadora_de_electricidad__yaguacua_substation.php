<h2>Yaguacua Substation</h2>

<div id="project-customer">Transportadora de Electricidad</div>
<div id="project-location">Tarija, Bolivia</div>

<ul>
  <li>
    Construction and assembly for Yaguacua  Substation, 230/69 kV, 75 MVA autotransformers bank, reactors bank  for 230 Transmission Line, for the new 100 MW Chaco Thermoelectric  Power Plant, according the following:
    <ul>
      <li>Civil works including: excavations, back fill, foundations, ground grid, drainage system, cable trench, firewalls for transformers and reactors, etc.</li>
      <li>Electromechanical assembly for all 230 and 69 kV High Voltage equipment, lattice steel structures for 230 and 69 kV bus bar system, power cable, control cabling and wiring terminations, etc.</li>
    </ul>
  </li>
</ul>

